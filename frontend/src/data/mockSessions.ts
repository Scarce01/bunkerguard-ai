import type { Session, BDNRecord, MFMStream, MFMReading } from './types';

const generateMFMReadings = (finalQuantity: number, duration: number): MFMReading[] => {
  const readings: MFMReading[] = [];
  const intervalMinutes = 5;
  const intervals = duration / intervalMinutes;

  for (let i = 0; i <= intervals; i++) {
    const progress = i / intervals;
    readings.push({
      timestamp: new Date(Date.now() - (duration - i * intervalMinutes) * 60000).toISOString(),
      cumulativeMass: finalQuantity * progress,
      massFlowRate: 125.3 + Math.random() * 10 - 5,
      density: 991.5 + Math.random() * 2 - 1,
      temperature: 42.3 + Math.random() * 1 - 0.5,
    });
  }

  return readings;
};

